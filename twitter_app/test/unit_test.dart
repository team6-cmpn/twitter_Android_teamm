import 'package:test/test.dart';
import '../lib/model/text_field_validation.dart';

void main() {
  test('Name -Empty- return error string', () {
    var result = nameValidator.validate('');
    expect(result, 'Enter correct name Alphabticals and underscore only');
  });

  test('Name -Non-Empty- return null', () {
    var result = nameValidator.validate('AnyName');
    expect(result, null);
  });
}
